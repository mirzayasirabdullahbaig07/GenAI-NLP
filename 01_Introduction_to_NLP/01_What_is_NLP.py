# ============================================================
# WHAT IS NLP?
# ============================================================

# Natural Language Processing (NLP) is a subfield of
# linguistics, computer science, and artificial intelligence (AI)
# that focuses on the interaction between computers and human language.
# It involves programming computers to process, analyze,
# understand, and generate natural language data.


# ============================================================
# WHAT IS NATURAL LANGUAGE?
# ============================================================

# In linguistics, neuropsychology, and philosophy of language,
# a natural language is any language that has evolved naturally
# among humans through use and repetition without conscious planning.

# Examples:
# - English
# - Urdu
# - Arabic
# - Sign languages

# Natural language can exist in different forms:
# - Spoken language
# - Written text
# - Sign language

# Natural languages are different from:
# - Formal languages (e.g., programming languages like Python, Java)
# - Constructed languages (e.g., Esperanto)


