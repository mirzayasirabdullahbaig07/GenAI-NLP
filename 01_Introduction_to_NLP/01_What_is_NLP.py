# ============================================================
# COMMON NLP TASKS
# ============================================================

# 1. Text / Document Classification
#    Example: Classifying emails as spam or not spam.

# 2. Sentiment Analysis
#    Example: "The movie was amazing!" → Positive

# 3. Information Retrieval
#    Example: Search engines retrieving relevant documents.

# 4. Part-of-Speech (POS) Tagging
#    Example: "She runs fast"
#    She (Pronoun), runs (Verb), fast (Adverb)

# 5. Named Entity Recognition (NER)
#    Example: "Elon Musk founded SpaceX."
#    Elon Musk (Person), SpaceX (Organization)

# 6. Language Detection
#    Example: Detecting whether text is English or French.

# 7. Machine Translation
#    Example: English → Spanish translation.

# 8. Question Answering (QA)
#    Example: "Who is the president of France?"

# 9. Text Summarization
#    - Extractive summarization
#    - Abstractive summarization

# 10. Topic Modeling
#     Discovering hidden topics in documents.

# 11. Text Generation
#     Generating human-like text.

# 12. Spell Checking and Grammar Correction

# 13. Text Parsing
#     Analyzing grammatical structure of sentences.

# 14. Speech-to-Text (ASR)
#     Converting speech into written text.

# 15. Text-to-Speech (TTS)
#     Converting text into spoken words.


# ============================================================
# APPROACHES TO NLP
# ============================================================

# 1. Heuristic (Rule-Based) Methods
# 2. Machine Learning Methods
# 3. Deep Learning Methods


# ============================================================
# 1. HEURISTIC (RULE-BASED) APPROACHES
# ============================================================

# These approaches use manually written rules and patterns.

# Examples:
# - Regular Expressions (RegEx)
# - WordNet
# - Open Mind Common Sense

# Advantages:
# - Fast and simple
# - Easy to implement for small tasks
# - No training data required

# Disadvantages:
# - Hard to scale
# - Difficult to maintain
# - Cannot handle language variation well


# ============================================================
# 2. MACHINE LEARNING APPROACHES
# ============================================================

# In ML, rules are automatically learned from data.

# Text must be converted into numerical form:
# - Bag of Words (BoW)
# - TF-IDF
# - Word Embeddings

# Common Algorithms:
# - Naive Bayes
# - Logistic Regression
# - Support Vector Machine (SVM)
# - Hidden Markov Model (HMM)
# - Latent Dirichlet Allocation (LDA)

# Advantages:
# - Better generalization
# - Learns patterns from data
# - Works well for many real-world problems

# Disadvantages:
# - Requires labeled data
# - Feature engineering needed


# ============================================================
# 3. DEEP LEARNING APPROACHES
# ============================================================

# Deep learning automatically learns features from raw text.

# Unlike traditional ML, deep learning models
# capture sequential and contextual information.

# Key Idea:
# - No manual feature engineering
# - Learns hierarchical representations

# Algorithms:
# - RNN (Recurrent Neural Network)
# - LSTM (Long Short-Term Memory)
# - GRU (Gated Recurrent Unit)
# - CNN (for text classification)
# - Transformers (e.g., BERT, GPT)
# - Autoencoders

# Advantages:
# - Captures context
# - Handles large datasets
# - State-of-the-art performance

# Disadvantages:
# - Requires large data
# - Computationally expensive


# ============================================================
# CHALLENGES IN NLP
# ============================================================

# 1. Ambiguity
# Example:
# "I saw a boy on the beach with my binoculars."
# (Who has the binoculars?)

# 2. Contextual Meaning
# "I ran to the store because we ran out of milk."
# (Two meanings of "ran")

# 3. Colloquialisms and Slang
# "It's a piece of cake." (means easy)
# "I'm pulling your leg." (means joking)

# 4. Synonyms
# "Big" vs "Large"

# 5. Irony and Sarcasm
# "That's just what I needed today!"
# (Could mean frustration)

# 6. Spelling Errors
# "Recieve" instead of "Receive"

# 7. Creativity
# Poems, metaphors, humor

# 8. Multilingual Diversity
# Different grammar structures across languages

# 9. Code-Switching
# Mixing languages in one sentence:
# "Kal meeting hai at 5 PM."

# 10. Low-Resource Languages
# Limited training data available.


